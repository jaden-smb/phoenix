// tools/common/debug_font.h — a real 5x7 bitmap font (digits, punctuation, A-Z) rendered
// into a 128x32 RGBA atlas of 8x8 cells (16 cols x 4 rows; cell 0 == ASCII 32, so the
// engine's BitmapFont defaults — first_char=32, cols=16 — sample it directly). Host-only:
// shared by the example's asset bake and the GUI editors, so every tool draws the same text
// through the engine UI without shipping a font asset. Undefined cells stay transparent.
#ifndef PHX_TOOLS_DEBUG_FONT_H
#define PHX_TOOLS_DEBUG_FONT_H

#include <cstdint>

namespace phxtool {

constexpr int kDebugFontW = 128, kDebugFontH = 32;

// Fills `font` (kDebugFontW * kDebugFontH RGBA8 texels) with the glyph atlas.
// Each glyph row is 5 bits, MSB = leftmost; glyphs sit at cell (ascii - 32) with a
// 1px top/left pad inside their 8x8 cell.
inline void build_debug_font(uint32_t* font) {
    struct GlyphDef { char c; uint8_t r[7]; };
    static const GlyphDef kGlyphs[] = {
        {'0',{0x0E,0x11,0x13,0x15,0x19,0x11,0x0E}}, {'1',{0x04,0x0C,0x04,0x04,0x04,0x04,0x0E}},
        {'2',{0x0E,0x11,0x01,0x02,0x04,0x08,0x1F}}, {'3',{0x1F,0x02,0x04,0x02,0x01,0x11,0x0E}},
        {'4',{0x02,0x06,0x0A,0x12,0x1F,0x02,0x02}}, {'5',{0x1F,0x10,0x1E,0x01,0x01,0x11,0x0E}},
        {'6',{0x06,0x08,0x10,0x1E,0x11,0x11,0x0E}}, {'7',{0x1F,0x01,0x02,0x04,0x08,0x08,0x08}},
        {'8',{0x0E,0x11,0x11,0x0E,0x11,0x11,0x0E}}, {'9',{0x0E,0x11,0x11,0x0F,0x01,0x02,0x0C}},
        {':',{0x00,0x04,0x00,0x00,0x00,0x04,0x00}}, {'!',{0x04,0x04,0x04,0x04,0x04,0x00,0x04}},
        {'.',{0x00,0x00,0x00,0x00,0x00,0x0C,0x0C}}, {'-',{0x00,0x00,0x00,0x1F,0x00,0x00,0x00}},
        {'A',{0x0E,0x11,0x11,0x1F,0x11,0x11,0x11}}, {'B',{0x1E,0x11,0x11,0x1E,0x11,0x11,0x1E}},
        {'C',{0x0E,0x11,0x10,0x10,0x10,0x11,0x0E}}, {'D',{0x1C,0x12,0x11,0x11,0x11,0x12,0x1C}},
        {'E',{0x1F,0x10,0x10,0x1E,0x10,0x10,0x1F}}, {'F',{0x1F,0x10,0x10,0x1E,0x10,0x10,0x10}},
        {'G',{0x0E,0x11,0x10,0x17,0x11,0x11,0x0F}}, {'H',{0x11,0x11,0x11,0x1F,0x11,0x11,0x11}},
        {'I',{0x0E,0x04,0x04,0x04,0x04,0x04,0x0E}}, {'J',{0x07,0x02,0x02,0x02,0x12,0x12,0x0C}},
        {'K',{0x11,0x12,0x14,0x18,0x14,0x12,0x11}}, {'L',{0x10,0x10,0x10,0x10,0x10,0x10,0x1F}},
        {'M',{0x11,0x1B,0x15,0x15,0x11,0x11,0x11}}, {'N',{0x11,0x11,0x19,0x15,0x13,0x11,0x11}},
        {'O',{0x0E,0x11,0x11,0x11,0x11,0x11,0x0E}}, {'P',{0x1E,0x11,0x11,0x1E,0x10,0x10,0x10}},
        {'Q',{0x0E,0x11,0x11,0x11,0x15,0x12,0x0D}}, {'R',{0x1E,0x11,0x11,0x1E,0x14,0x12,0x11}},
        {'S',{0x0F,0x10,0x10,0x0E,0x01,0x01,0x1E}}, {'T',{0x1F,0x04,0x04,0x04,0x04,0x04,0x04}},
        {'U',{0x11,0x11,0x11,0x11,0x11,0x11,0x0E}}, {'V',{0x11,0x11,0x11,0x11,0x11,0x0A,0x04}},
        {'W',{0x11,0x11,0x11,0x15,0x15,0x1B,0x11}}, {'X',{0x11,0x11,0x0A,0x04,0x0A,0x11,0x11}},
        {'Y',{0x11,0x11,0x0A,0x04,0x04,0x04,0x04}}, {'Z',{0x1F,0x01,0x02,0x04,0x08,0x10,0x1F}},
    };
    for (int i = 0; i < kDebugFontW * kDebugFontH; ++i) font[i] = 0u;   // transparent
    for (const auto& g : kGlyphs) {
        const int cell = int(g.c) - 32;
        const int cx = (cell % 16) * 8, cy = (cell / 16) * 8;
        for (int ry = 0; ry < 7; ++ry)
            for (int rx = 0; rx < 5; ++rx)
                if (g.r[ry] & (1 << (4 - rx)))
                    font[(cy + ry + 1) * kDebugFontW + (cx + rx + 1)] = 0xFFFFFFFFu;
    }
}

} // namespace phxtool
#endif // PHX_TOOLS_DEBUG_FONT_H
